    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        try{
            int codigo = Integer.parseInt(jtCodigo.getText());
            String nombre = jtNombre.getText();
            int anio = Integer.parseInt(jtAnio.getText());
            materia=new Materia(codigo, nombre, anio);
            if(listaMaterias.add(materia)){
                JOptionPane.showMessageDialog(this, "Materia Agregada con Exíto");
            }
            else{
                JOptionPane.showMessageDialog(this, "La materia ya Existe");
            }
        }catch(NumberFormatException nref){
            JOptionPane.showMessageDialog(this, "Error al agregar el codigo o el anio");
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

